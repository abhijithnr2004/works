# 1. first and last character

s = "Technology"

print(s[0],end = ",")

print(s[-1])



# 2. extract a word from a sentence

s = "MachineLearningModel"

print(s[7:15])



# 3. every third character

s = "ABCDEFGHIJKLMN"

print(s[0::3])



# 4. reverse the string

s = "OpenAI"

print(s[::-1])



# 5. remove first and last character

s = "Artificial"

print(s[1:-1])



# 6. first 5 characters

s = "Development"

print(s[:5])



# 7. last 4 characters

s = "Programming"

print(s[-4:])



# 8. extract middle part

s = "Environment"

print(s[2:7])



# 9. skip one character

s = "SlicingIndex"

print(s[0::2])
